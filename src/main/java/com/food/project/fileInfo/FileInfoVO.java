package com.food.project.fileInfo;

public class FileInfoVO {
	private long imgNum;
	private String fileName;
	private String oriName;
	private int kind;
	private long marketNum;
	private long reviewNum;
	private long menuNum;
	
	
	
	public long getImgNum() {
		return imgNum;
	}
	public void setImgNum(long imgNum) {
		this.imgNum = imgNum;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getOriName() {
		return oriName;
	}
	public void setOriName(String oriName) {
		this.oriName = oriName;
	}
	public int getKind() {
		return kind;
	}
	public void setKind(int kind) {
		this.kind = kind;
	}
	public long getMarketNum() {
		return marketNum;
	}
	public void setMarketNum(long marketNum) {
		this.marketNum = marketNum;
	}
	public long getReviewNum() {
		return reviewNum;
	}
	public void setReviewNum(long reviewNum) {
		this.reviewNum = reviewNum;
	}
	public long getMenuNum() {
		return menuNum;
	}
	public void setMenuNum(long menuNum) {
		this.menuNum = menuNum;
	}
	
	
	
}
